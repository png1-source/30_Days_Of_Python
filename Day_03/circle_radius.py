import math

radius_of_circle = int(input('Enter the radius of the circle: '))

circle_area = math.pi * radius_of_circle * radius_of_circle # Calculates the area of the circle
circle_circumference = 2 * math.pi * radius_of_circle # Caculates the circumference of the circle

print(f'The area of your circle is {circle_area}') # Circle area output
print(f' The circumference of your circle is {circle_circumference}') # Circle circumference output 

