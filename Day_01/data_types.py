print(type(123)) # This is the integer type
print(type(3.14)) # This is the float type
print(type(3 + 4j)) # This is the complex type
print(type("Hello, World!")) # This is the string type
print(type([1, 2, 3])) # This is the list type
print(type((1, 2, 3))) # This is the tuple type
print(type({1: "one", 2: "two", 3: "three"})) # This is the dictionary type
print(type({1, 2, 3})) # This is the set type
print(type(True)) # This is the boolean type
print(type(None)) # This is the NoneType




